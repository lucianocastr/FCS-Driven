function FhelpGui() {
	var self = this;
	this.filterTitles = [
		"BW@1dB&nbsp;(KHz)",
		"BW@3dB&nbsp;(KHz)",
		"BW@10dB&nbsp;(KHz)",
		"Delay&nbsp;(us)"
	];
	this.adjFilterTitles = [
		"BW@1dB&nbsp;(MHz)",
		"BW@3dB&nbsp;(MHz)",
		"BW@10dB&nbsp;(MHz)",
		"Delay&nbsp;(us)"
	];
	this.filterTypes = FilterTypes;
	this.adjFilterTypes = AdjFilterTypes;
	this.create = function() {
		var box = document.getElementById("fhFiltBox");
		remove_children(box);
		this.createFHelp(box);
	}
	this.drawPlot = function(el,filt){
		var w = 400; el.width = w;
		var h = 300; el.height = h;
		var reflevel = filt.reflevel[0];
		var reflevel2 = filt.reflevel[1];
		var nX = 10;
		var nY = filt.nY;
		var lblx = -filt.scaleX / 2;
		var stepX = filt.scaleX / nX;
		var stepY = filt.scaleY[0] / nY;
		var stepY2 = filt.scaleY[1] / nY;
		var fs = filt.scaleY[0];
		var fs2 = filt.scaleY[1];
		var lbly = reflevel;
		var lbly2 = reflevel2;
		var ctx = el.getContext("2d");
		ctx.fillStyle = "black";
		ctx.textAlign = "center";
		ctx.font = "12px Arial";
		ctx.fillText(filt.title , 200,15);
		ctx.font = "11px Arial";
		for (i = 0; i <= nX; i ++) {
			ctx.beginPath();
			if (i==0 || i==nX)
				ctx.setLineDash([]);
			else
				ctx.setLineDash([5, 5]);
			ctx.moveTo((i+1)*w/(nX+2), h/(nY+2));
			ctx.lineTo((i+1)*w/(nX+2), h*(nY+1)/(nY+2));
			ctx.stroke();
			var s = lblx;
			if (Math.round(lblx)!=lblx) s=lblx.toFixed(1);
			ctx.fillText(s , Math.round( (i+1)*w/(nX+2)),290);
			lblx = lblx +stepX;		
		}
		for (i = 0; i <= nY; i ++) {
			ctx.beginPath();
			if (i==0 || i==nY)
				ctx.setLineDash([]);
			else
				ctx.setLineDash([5, 5]);
			ctx.moveTo(w/(nX+2), (i+1)*h/(nY+2));
			ctx.lineTo(w*(nX+1)/(nX+2), (i+1)*h/(nY+2));
			ctx.stroke();
			ctx.fillText(lbly, 18,Math.round(5+(i+1)*h/(nY+2)));
			ctx.fillText(lbly2, 380,Math.round(5+(i+1)*h/(nY+2)));
			lbly = lbly - stepY;
			lbly2 = lbly2 -stepY2;
		}
		ctx.fillText(filt.unitsX , w/2,h);
		ctx.rotate(-Math.PI/2);
		ctx.fillText("dB" ,-h/2,10);
		ctx.rotate(Math.PI);
		ctx.fillText("us" ,h/2,-390);
		ctx.rotate(-Math.PI/2);
		//response
		ctx.strokeStyle = "blue";
		var vals = filt.resp;
		for (i=vals.length-1;i>=0;i--) //resp sólo contiene media curva
			vals.push(filt.resp[i]); //se completa por simetría
		var n = vals.length;
		for (i = 0; i <= n-1; i ++) {
			ctx.beginPath();
			ctx.moveTo(w*(nX*(i)/(n-1)+1)/(nX+2), h*(nY*(reflevel+vals[i]/10.0)/fs+1)/(nY+2)); //se cambia signo a val (ocupa menos)
			ctx.lineTo(w*(nX*(i+1)/(n-1)+1)/(nX+2), h*(nY*(reflevel+vals[i+1]/10.0)/fs+1)/(nY+2));
			ctx.stroke();
		}
		//delay
		ctx.strokeStyle = "red";
		var vals = filt.delay;
		for (i=vals.length-1;i>=0;i--) //resp sólo contiene media curva
			vals.push(filt.delay[i]); //se completa por simetría
		var n = vals.length;
		for (i = 0; i <= n-1; i ++) {
			ctx.beginPath();
			ctx.moveTo(w*(nX*(i)/(n-1)+1)/(nX+2), h*(nY*(reflevel2-vals[i]/10.0)/fs2+1)/(nY+2));
			ctx.lineTo(w*(nX*(i+1)/(n-1)+1)/(nX+2), h*(nY*(reflevel2-vals[i+1]/10.0)/fs2+1)/(nY+2));
			ctx.stroke();
		}

		
	}
	this.createFHelp = function(box) {
		var n = 0;
		var nr_rows;

		for (var nbadj = 0; nbadj < 2; nbadj++) {
			nr_rows = nbadj==0 ? self.filterTypes.length : self.adjFilterTypes.length;
			for (var i = 0; i < nr_rows; ++i) {
				var tr = document.createElement("tr");
				box.appendChild(tr);
				
				var filt = (nbadj == 0 ? self.filterTypes: self.adjFilterTypes);
				var td = document.createElement("td");
				tr.appendChild(td);
				//var el = $('<canvas/>').prop({"width":"400","height":"300"}).html('Your browser does not support canvas');
				var el = document.createElement("canvas");
				el.style.width = "400px";
				el.style.height = "300px";
				el.innerHTML = 'Your browser does not support canvas';
				el.id = filt[i].name;
				td.appendChild(el);
				var resp = nbadj==0 ?FreqResponses[i] : adjFreqResponses[i];
				this.drawPlot(el,resp);
				td = document.createElement("td");
				tr.appendChild(td);
				var fdata = filt[i].data;
				var dataTble = self.createFhDataTable(fdata,nbadj != 0);
				var dbox = document.createElement("div");
				dbox.appendChild(dataTble);
				td.appendChild(dbox);
			}
		}
	}
	this.createFhDataTable = function(fdata,isAdj) {
		var tbl = document.createElement("table");
		tbl.className = "fhelp";
		var tb = document.createElement("tbody");
		tbl.appendChild(tb);
		var titles = isAdj ? self.adjFilterTitles : self.filterTitles;
		for (var r = 0; r < titles.length; ++r) {
			var tr = document.createElement("tr");
			tb.appendChild(tr);
			var td = document.createElement("th");
			td.innerHTML = titles[r];
			td.className = "fhelp";
			tr.appendChild(td);
			var td = document.createElement("td");
			td.innerHTML = fdata[r].toFixed((!isAdj || r==(titles.length-1))?1:2); //los datos de BW(MHz) con 2decimales
			td.className = "fhelp";
			tr.appendChild(td);
		}
		return tbl;
	}
}

